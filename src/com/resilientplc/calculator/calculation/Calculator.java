package com.resilientplc.calculator.calculation;

public class Calculator {

    public int add (int x, int y){
        return x + y;
    }

    public int subtract(int x, int y){
        return x - y;
    }

    public int multiple(int x, int y){
        return x * y;
    }

    public int divide(int x, int y){ return x / y; }

}
